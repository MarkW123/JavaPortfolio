import java.util.Scanner;

public class NumberLoops3{
	  
	public static void main (String [] args){
		
		for(int i=1; i <=15; i++){
			
				for(int j = 6; j>0; j--){
					System.out.println(i + " " + j);
				}
			}
		
		
	}  // end of main class
	
	
	
}  // end of public class

