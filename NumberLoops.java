import java.util.Scanner;

public class NumberLoops{
	
	public static void main (String [] args){
	
		for (int i = 0; i <= 10; i++){  // outer loop
			
			for(int x = 0; x <= 7; x++){  // inner loop
                            System.out.println(x);
			}
                            System.out.println(i);

		}
	
	}  // end of main class
	
}  // end


